#Using augmented assignment operators
spam = 42
spam += 1
print(spam)

#List of augmented statements
spam+=1 #spam = spam + 1
spam-=1 #spam = spam - 1
spam*=1 #spam = spam * 1
spam/=1 #spam = spam / 1
spam%=1 #spam = spam % 1

#Also can be used for strings
spam2='Hello'
spam2+=' world'
print(spam2) # 'Hello world'

bacon = ['Zophie']
bacon*=3
print(bacon) #['Zophie', 'Zophie', 'Zophie']